#include <stdio.h>
#include <string.h>

int	ft_strcmp(char *s1, char *s2)
{
	int x;

	x = 0;
	while (s1[x] == s2[x] && s1[x] != '\0' && s2[x] != '\0')
		x++;
	return (s1[x] - s2[x]);
}

int main()
{
	char x[] = "Erick";
	char y[] = "Erick";
	printf("%d", ft_strcmp(x, y));
}
