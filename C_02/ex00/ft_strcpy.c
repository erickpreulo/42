#include <stdio.h>
#include <string.h>

char	*ft_strcpy(char *dest, char *src)
{
	char x;

	x = 0;
	while (src[x] != '\0')
	{
		dest[x] = src[x];
		x++;
	}
	dest[x] = '\0';
	return (dest);
}
