#include <stdio.h>

void	desafio()
{
	char x = 'a';
	int i = 1;
	while (x <= 'z' && (i <= 26))
	{
		printf("%c%i ", x, i);
		x++;
		i++;
	}
}

int main()
{
	desafio();
}
